#include <stdio.h>

int main() {
    int a = 15, b = 4;

    printf("a = %d, b = %d\n", a, b);

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);   // integer division
    printf("a %% b = %d\n", a % b);  // modulus

    return 0;
}